"""
Модуль генерации названия обращения.

В базе данных каждое обращение имеет краткий заголовок (title).
Пользователь его не вводит — он создаётся автоматически
на основе первых символов описания.

Назначение заголовка:
- облегчить поиск обращения в базе;
- сделать записи читаемыми;
- гарантировать уникальность за счёт временной метки.
"""
from datetime import datetime
import re


def generate_bug_title(description: str) -> str: 
    """
    Генерирует короткое название обращения.

    Алгоритм:
    1. Удаляет лишние пробелы и переносы строк.
    2. Берёт первые 40 символов описания.
    3. Добавляет текущую дату и время.

    Пример результата:

        "При открытии отчета... [20260705-143221]"

    Благодаря временной метке даже одинаковые описания
    получают разные заголовки.
    """
    text = re.sub(r"\s+", " ", description).strip()
    # Ограничиваем длину заголовка
    if len(text) > 40:
        text = text[:40].rstrip()
    # Добавляем время создания
    timestamp = datetime.now().strftime("%Y%m%d-%H%M%S")

    return f"{text} [{timestamp}]"

